function pickRandom(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

function randomAge() {
  return 8 + Math.floor(Math.random() * (120 - 8 + 1));
}

function formatAge(years) {
  const mod10 = years % 10;
  const mod100 = years % 100;
  if (mod10 === 1 && mod100 !== 11) return `${years} год`;
  if (mod10 >= 2 && mod10 <= 4 && (mod100 < 10 || mod100 >= 20)) {
    return `${years} года`;
  }
  return `${years} лет`;
}

function is18(scenarioId) {
  return scenarioId === "vulgar";
}

function getCardTypes(scenarioId) {
  return is18(scenarioId) ? CARD_TYPES_18PLUS : CARD_TYPES;
}

function getCardPools(scenarioId) {
  return is18(scenarioId) ? CARD_POOLS_18PLUS : CARD_POOLS;
}

function getProfLevels(scenarioId) {
  return is18(scenarioId) ? PROFESSION_LEVELS_18PLUS : PROFESSION_LEVELS;
}

function rollProfessionCard(scenarioId) {
  const pools = getCardPools(scenarioId);
  const levels = getProfLevels(scenarioId);
  const profession = pickRandom(pools.profession);
  const level = pickRandom(levels);
  return {
    value: `${profession} (${level})`,
    profession,
    professionLevel: level,
  };
}

function rollCardValue(key, scenarioId) {
  const pools = getCardPools(scenarioId);
  if (key === "gender_age") {
    const gender = pickRandom(pools.gender);
    return `${gender}, ${formatAge(randomAge())}`;
  }
  return pickRandom(pools[key]);
}

function dealPlayerCards(scenarioId) {
  const types = getCardTypes(scenarioId);
  const levels = getProfLevels(scenarioId);
  const pools = getCardPools(scenarioId);
  return types.map(({ key, label }) => {
    if (key === "profession") {
      const prof = rollProfessionCard(scenarioId);
      return {
        type: key,
        label,
        value: prof.value,
        profession: prof.profession,
        professionLevel: prof.professionLevel,
        opened: false,
      };
    }
    if (is18(scenarioId) && key === "health") {
      const condition = pickRandom(pools.health);
      const level = pickRandom(levels);
      return {
        type: key,
        label,
        value: `${condition} (${level})`,
        condition,
        conditionLevel: level,
        opened: false,
      };
    }
    return {
      type: key,
      label,
      value: rollCardValue(key, scenarioId),
      opened: false,
    };
  });
}

